    private void btnListeActionPerformed() {                                         
        ArrayList<Client> array = Service.listeClient();
        int i, size = array.size();
        data = new Object[size][8];
        btnEditer.setVisible(false);
        btnSupprimer.setVisible(false);
        btnActDes.setVisible(false);
        labelRendu.setText("");
        Client cli = new Client();
        for (i = 0; i < size; i++) {
            cli = array.get(i);
            data[i][0] = cli.getNumCompt();
            data[i][1] = cli.getNom().toUpperCase();
            data[i][2] = cli.getPrenom().toUpperCase();
            data[i][3] = cli.getSexe().toUpperCase();
            data[i][4] = cli.getCni();
            data[i][5] = cli.getSolde();
            data[i][6] = (cli.getEtat() > 0 ? true : false);
            data[i][7] = (cli.getEtat() > 0 ? "activer" : "désactiver");
        }
        tableListe.setCellEditor(null);
        tableListe.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[]{
            "COMPTE", "NOM", "PRENOM", "SEXE", "CNI", "SOLDE", "ETAT", "ACT/DES"
        }) {
            /**
			 * 
			 */
			private static final long serialVersionUID = 1L;
			Class[] types = new Class[]{
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.Integer.class, java.lang.Object.class, java.lang.Object.class
            };
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return data[0][columnIndex].getClass();

            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
        this.tableListe.setRowHeight(30);


    }                                        

    private void btnValiderNouveauActionPerformed(java.awt.event.ActionEvent evt) {                                                     
        if(validerMontant(textFieldNvSolde.getText().trim())&&validerNom(textFieldNvNom.getText().trim())&& 
                validerNom(textFieldNvPrenom.getText().trim())&&validerCni(textFieldNvCni.getText().trim())){
        if (edit == false) {
        	client = new Client(textFieldNvCompte.getText().trim(),
                    textFieldNvNom.getText().trim(), textFieldNvPrenom.getText().trim(),
                    (String) comboSexe.getSelectedItem(), textFieldNvCni.getText().trim(),
                    "0000", Integer.parseInt(textFieldNvSolde.getText().trim()), 1,0);
            client.save();
            btnListeActionPerformed();
        } else {
        	client = new Client(textFieldNvCompte.getText().trim(),
                    textFieldNvNom.getText().trim(), textFieldNvPrenom.getText().trim(),
                    (String) comboSexe.getSelectedItem(), textFieldNvCni.getText().trim(),
                    "0000", Integer.parseInt(textFieldNvSolde.getText().trim()), 1,client.getEtatSupp());
            client.update();
            btnListeActionPerformed();
            edit = false;
        }
        labelRendu.setText(Service.messageRendu);
        }else{labelRendu.setText("Un de champ est incorrect : veuillez vérifier SVP"); }
        textFieldNvCompte.setText("");
        textFieldNvNom.setText("");
        textFieldNvPrenom.setText("");
        textFieldNvCni.setText("");
        textFieldNvSolde.setText("");
    }                                                 

