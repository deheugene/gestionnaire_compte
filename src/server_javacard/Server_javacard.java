    private void btnEditerActionPerformed(java.awt.event.ActionEvent evt) {                                          
        edit = true;
        labelRendu.setText("");
        int select = tableListe.getSelectedRow();
        if (select >= 0) {
            textFieldNvCompte.setText((String) data[select][0]);
            textFieldNvCompte.setEditable(false);
            textFieldNvNom.setText((String) data[select][1]);
            textFieldNvPrenom.setText((String) data[select][2]);
            if("FÉMININ".equals(data[select][3])){
            	comboSexe.setModel(new javax.swing.DefaultComboBoxModel(new String[] {"féminin" , "masculin" }));
            }else{
            	comboSexe.setModel(new javax.swing.DefaultComboBoxModel(new String[] { "masculin", "féminin" }));
            }
            textFieldNvCni.setText("" + data[select][4]);
            textFieldNvSolde.setText("" + data[select][5]);
        }
    }  
    
