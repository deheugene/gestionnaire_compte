    private boolean validerMontant(String text) {
		return text.matches("^[1-9][0-9]*$")&& (Integer.parseInt(text)>0);
	}
    private boolean validerCni(String text) {
		return text.matches("^[1-9][0-9]*$")&& (Integer.parseInt(text)>0) && text.length()==9;
	}
    private boolean validerNom(String text) {
		return (text.matches("^[a-z A-Z é è É È ë ï Ë Ï]+$")&& text.length()>=3);
	}
